//for loop
var repeatCount = 10;
for(var i = 0; i < repeatCount;i++){
    console.log("Iteration " + i);
}

//while loop
var condition = 10
while(condition > 0){
    console.log("Condition " + condition);
    condition--;
}

//do-while loop
var condition = 10
do{
    console.log("Condition " + condition);
    condition++;
}while(condition <= 0)