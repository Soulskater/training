// Függvény definíciók

function sum(number1, number2, number3){
    var result = number1 + number2 + number3;
    return result;
}

function multiply(number1, number2, number3){
    var result = number1 * number2 * number3;
    return result;
}

function multiply(number1, number2){
    return number1 * number2;
}